package es.fpc.Clase;

public class VariablesConDecimales {
    public static void main(String[] args) {
        double x;
        double y;
        x = 7;
        y = 25.01;
        System.out.println("El valor de x es " + x);
        System.out.println("El valor de y es " + y);

        System.out.println("El mayor valor int es " + Integer.MAX_VALUE);
        System.out.println("El mayor valor int es " + Integer.MIN_VALUE);

        float f1 = 3.65437895738497f; //a veces se pone la f para indicar que es flotante.
        double d1 = 3.78956748977348978439537458934d; //tmb se pone d al final a veces.
        
        System.out.println(f1);
        System.out.println(d1);
    }
    
}
